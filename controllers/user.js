const User = require('../models/User');


generateToken = (user) => {
  return jwt.sign(user, process.env.SESSION_SECRET, {
    expiresIn: 10080 // in seconds
  });
};

exports.generateToken = (user) => {
  return jwt.sign(user, process.env.SESSION_SECRET, {
    expiresIn: 10080 // in seconds
  });
};

// Set user info from request
setUserInfo = (req) => {
  return {
    _id: req._id,
    email: req.email
  };
};

